def t(lang: str | Language, key: str, **kwargs: Any) -> str:
    lang_key = lang.value if isinstance(lang, Language) else lang
    bucket = TRANSLATIONS.get(lang_key) or TRANSLATIONS["en"]
    text = bucket.get(key) or TRANSLATIONS["en"].get(key) or key
    return text.format(**kwargs)


def language_name(lang: str | Language) -> str:
    code = lang.value if isinstance(lang, Language) else lang
    return t(code, f"language_name_{code}")


def parse_language_from_text(text: str) -> Language | None:
    text = text.strip()
    mapping = {
        "English": Language.EN,
        "မြန်မာ": Language.MY,
        "Русский": Language.RU,
    }
    return mapping.get(text)


def parse_gender(text: str) -> Gender | None:
    normalized = text.strip().lower()
    mapping = {
        "i’m male": Gender.MALE,
        "i'm male": Gender.MALE,
        "ကျွန်တော် ယောက်ျားပါ": Gender.MALE,
        "я мужчина": Gender.MALE,
        "i’m female": Gender.FEMALE,
        "i'm female": Gender.FEMALE,
        "ကျွန်မ မိန်းကလေးပါ": Gender.FEMALE,
        "я женщина": Gender.FEMALE,
    }
    return mapping.get(normalized)


def parse_looking_for(text: str) -> LookingFor | None:
    normalized = text.strip().lower()
    mapping = {
        "men": LookingFor.MEN,
        "women": LookingFor.WOMEN,
        "everyone": LookingFor.EVERYONE,
        "ယောက်ျား": LookingFor.MEN,
        "မိန်းကလေး": LookingFor.WOMEN,
        "အားလုံး": LookingFor.EVERYONE,
        "мужчины": LookingFor.MEN,
        "женщины": LookingFor.WOMEN,
        "все": LookingFor.EVERYONE,
    }
    return mapping.get(normalized)


def report_reason_label(lang: str | Language, reason: ReportReason) -> str:
    return t(lang, f"reason_{reason.value}")


def parse_report_reason(text: str) -> ReportReason | None:
    normalized = text.strip().lower()
    for reason in ReportReason:
        for lang in ("en", "my", "ru"):
            label = TRANSLATIONS[lang].get(f"reason_{reason.value}", "").lower()
            if label == normalized:
                return reason
    return None
